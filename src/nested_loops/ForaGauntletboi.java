package nested_loops;

public class ForaGauntletboi {
public static void main(String[] args) {

	for (int i = 0; i < 100; i++) {
		System.out.println(i);
	}
	for (int j = 100; j < 0; j++) {
		System.out.println(j);
	}
	for (int a = 2; a < 100; a++) {
		if(a % 2 == 0) {
	System.out.println(a);
		}
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
